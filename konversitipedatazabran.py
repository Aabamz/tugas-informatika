# Data dari sensor suhu dalam bentuk teks
suhu_sensor = '20'
print(suhu_sensor, 'tipe data suhu_sensor sebelumnya =', type(suhu_sensor))

# Konversi ke tipe integer
print(int(suhu_sensor), 'tipe data suhu_sensor =', type(int(suhu_sensor)))

# Konversi ke tipe float
print(float(suhu_sensor), 'tipe data suhu_sensor =', type(float(suhu_sensor)))

# Konversi ke tipe kompleks
print(complex(suhu_sensor), 'tipe data suhu_sensor =', type(complex(suhu_sensor)))
