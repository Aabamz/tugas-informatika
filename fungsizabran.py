# Contoh skor siswa
siswa1, siswa2 = 4, -8
print('Skor mutlak dari siswa 1 adalah =', abs(siswa1))
print('Skor mutlak dari siswa 2 adalah =', abs(siswa2))
print()

# Tiga nilai ujian dari seorang siswa
ujian1, ujian2, ujian3 = 5, 3, 9
print('Skor Ujian 1 =', ujian1, 'Ujian 2 =', ujian2, 'Ujian 3 =', ujian3)
print('Skor Tertinggi adalah =', max(ujian1, ujian2, ujian3))
print('Skor Terendah adalah =', min(ujian1, ujian2, ujian3))
print()

# Skor tugas
tugas = [2, 6, 10]
print('Skor tugas:', tugas)
print('Skor Tertinggi dari daftar tugas adalah =', max(tugas))
print('Skor Terendah dari daftar tugas adalah =', min(tugas))
