a_zabran = 10
b_zabran = 5
op_zabran = '/' 
hasil_zabran = 0

if op_zabran == '+':
    hasil_zabran = a_zabran + b_zabran
elif op_zabran == '-':
    hasil_zabran = a_zabran - b_zabran
elif op_zabran == '*':
    hasil_zabran = a_zabran * b_zabran
elif op_zabran == '/':
    hasil_zabran = a_zabran / b_zabran
elif op_zabran == '//':
    hasil_zabran = a_zabran // b_zabran

print("Hasil=",hasil_zabran)